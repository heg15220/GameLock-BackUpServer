import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const outputFile = path.join(repoRoot, "src", "games", "knowledge", "timelineEventBank.compact.generated.js");
const localeOutputDir = path.join(repoRoot, "src", "games", "knowledge", "timelineEventBankCompactLocales");

const DEFAULT_CATEGORY_LIMIT = 160;
const CATEGORY_LIMITS = new Map([
  ["historical-event", 220],
  ["video-game", 220],
  ["film", 180],
  ["music-album", 180],
  ["song", 180],
  ["sports-event", 180],
  ["software", 180],
  ["election", 180],
  ["political-party", 180],
  ["war", 180],
  ["programming-language", 180],
  ["satellite", 160],
  ["restaurant", 140],
  ["military-conflict", 160],
  ["treaty", 138],
  ["space-mission", 87],
  ["food-company", 10],
]);

const extractCategoryId = (eventId) => {
  if (typeof eventId !== "string" || !eventId.startsWith("wd-")) return "uncategorized";
  const qMarker = eventId.lastIndexOf("-q");
  if (qMarker <= 3) return "uncategorized";
  return eventId.slice(3, qMarker);
};

const compareEvents = (left, right) => {
  if ((left.year ?? 0) !== (right.year ?? 0)) {
    return (left.year ?? 0) - (right.year ?? 0);
  }
  return String(left.id || "").localeCompare(String(right.id || ""));
};

const resolveCategoryLimit = (categoryId) => CATEGORY_LIMITS.get(categoryId) ?? DEFAULT_CATEGORY_LIMIT;

const selectEvenlySpacedItems = (items, limit) => {
  if (items.length <= limit) {
    return [...items];
  }

  const sorted = [...items].sort(compareEvents);
  const selected = [];
  const usedIndexes = new Set();
  const lastIndex = sorted.length - 1;

  for (let slot = 0; slot < limit; slot += 1) {
    const targetIndex = Math.round((slot * lastIndex) / Math.max(1, limit - 1));
    let resolvedIndex = targetIndex;

    while (usedIndexes.has(resolvedIndex) && resolvedIndex < sorted.length) {
      resolvedIndex += 1;
    }
    while (usedIndexes.has(resolvedIndex) && resolvedIndex > 0) {
      resolvedIndex -= 1;
    }
    if (usedIndexes.has(resolvedIndex)) {
      continue;
    }

    usedIndexes.add(resolvedIndex);
    selected.push(sorted[resolvedIndex]);
  }

  return selected.sort(compareEvents);
};

const formatLocaleModule = (payload) => (
  `/* eslint-disable */\n`
  + `// Generated by scripts/generate-compact-timeline-bank.mjs. Do not edit manually.\n`
  + `export default Object.freeze(${JSON.stringify(payload)});\n`
);

const formatModule = (eventMeta, meta) => (
  `/* eslint-disable */\n`
  + `// Generated by scripts/generate-compact-timeline-bank.mjs. Do not edit manually.\n`
  + `export const COMPACT_GENERATED_TIMELINE_EVENT_META = Object.freeze(${JSON.stringify(eventMeta)});\n`
  + `export const COMPACT_GENERATED_TIMELINE_EVENTS_META = Object.freeze(${JSON.stringify(meta)});\n`
  + `export const COMPACT_GENERATED_TIMELINE_LOCALE_LOADERS = {\n`
  + `  "es": () => import("./timelineEventBankCompactLocales/es.generated.js"),\n`
  + `  "en": () => import("./timelineEventBankCompactLocales/en.generated.js")\n`
  + `};\n\n`
  + `const LOCALE_CACHE = new Map();\n`
  + `const LOCALE_PROMISES = new Map();\n\n`
  + `const normalizeLocaleKey = (locale) => (String(locale || "es").toLowerCase().startsWith("es") ? "es" : "en");\n\n`
  + `export const loadCompactGeneratedTimelineLocale = async (locale) => {\n`
  + `  const localeKey = normalizeLocaleKey(locale);\n`
  + `  const loader = COMPACT_GENERATED_TIMELINE_LOCALE_LOADERS[localeKey];\n`
  + `  if (!loader) return {};\n`
  + `  if (LOCALE_CACHE.has(localeKey)) return LOCALE_CACHE.get(localeKey) ?? {};\n`
  + `  if (!LOCALE_PROMISES.has(localeKey)) {\n`
  + `    LOCALE_PROMISES.set(localeKey, loader().then((module) => {\n`
  + `      const localeText = module.default ?? {};\n`
  + `      LOCALE_CACHE.set(localeKey, localeText);\n`
  + `      return localeText;\n`
  + `    }));\n`
  + `  }\n`
  + `  return LOCALE_PROMISES.get(localeKey) ?? {};\n`
  + `};\n`
);

const main = async () => {
  const { GENERATED_TIMELINE_EVENTS } = await import("../src/games/knowledge/timelineEventBank.generated.js");
  const grouped = new Map();

  for (const event of GENERATED_TIMELINE_EVENTS) {
    const categoryId = extractCategoryId(event.id);
    const current = grouped.get(categoryId) || [];
    current.push(event);
    grouped.set(categoryId, current);
  }

  const compactEvents = [];
  const selectedCounts = {};
  const originalCounts = {};

  for (const [categoryId, events] of [...grouped.entries()].sort((left, right) => left[0].localeCompare(right[0]))) {
    const limit = resolveCategoryLimit(categoryId);
    const selected = selectEvenlySpacedItems(events, limit);
    compactEvents.push(...selected);
    originalCounts[categoryId] = events.length;
    selectedCounts[categoryId] = selected.length;
  }

  const compactSorted = compactEvents.sort(compareEvents);
  const compactEventMeta = compactSorted.map(({ id, year, tags }) => ({ id, year, tags }));
  const compactLocaleText = {
    es: Object.fromEntries(
      compactSorted.map((event) => [
        event.id,
        {
          title: event.title?.es ?? "",
          summary: event.summary?.es ?? "",
        },
      ])
    ),
    en: Object.fromEntries(
      compactSorted.map((event) => [
        event.id,
        {
          title: event.title?.en ?? "",
          summary: event.summary?.en ?? "",
        },
      ])
    ),
  };
  const meta = {
    sourceCount: GENERATED_TIMELINE_EVENTS.length,
    compactCount: compactSorted.length,
    originalCounts,
    selectedCounts,
  };

  await fs.mkdir(localeOutputDir, { recursive: true });
  await fs.writeFile(outputFile, formatModule(compactEventMeta, meta), "utf8");
  await fs.writeFile(path.join(localeOutputDir, "es.generated.js"), formatLocaleModule(compactLocaleText.es), "utf8");
  await fs.writeFile(path.join(localeOutputDir, "en.generated.js"), formatLocaleModule(compactLocaleText.en), "utf8");
  console.log(JSON.stringify(meta, null, 2));
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
